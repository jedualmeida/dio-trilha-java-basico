public class Calculadora {
    /**
    * Este método é utilizado para somar dois números inteiros
    * @param numeroUm este é o primeiro parâmtero do método
    * @param numeroDois este é o segundo parâmtero do método
    * @return int o resultado deste método é a soma dois dois números
    */
    public int somar(int numeroUm, int numeroDois) {
        return numeroUm + numeroDois;

    }
    
}
